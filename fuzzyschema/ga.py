"""
fuzzyschema/ga.py
-----------------
Schema-generic pymoo GA harness for optimising a rule-only chromosome
(see chromosome.py), OR, when `mf_params_cls` is given, a combined
rule+MF chromosome that jointly optimises rules and membership-function
parameters. Application-agnostic: takes an injected
`fitness_fn(chromosome) -> score` to maximise, and (optionally) `rules_fn`
/ `mf_seed_fn` to seed the initial population from an existing expert
configuration.

What this module does NOT do (by design, left to the consuming application):
  - Compute the fitness itself (mAP, MSE, MCC, or anything else). fitness_fn
    receives the raw chromosome array (rule-only, or combined rule+MF when
    mf_params_cls is set) and is entirely responsible for decoding it --
    this module never builds an inference engine or touches ex_fuzzy.
  - Any domain-specific "final verbose evaluation" pass after the GA
    completes -- pass `final_eval_fn` if you want one; it is called with
    (best_chromosome, run_dir) after artefacts are saved.

MF-optimisation mode
---------------------
Pass `mf_params_cls` (a class generated by build_mf_params_class or
build_it2_mf_params_class -- see mf_params.py / mf_params_t2.py) to switch
from a rule-only chromosome to a combined rule+MF chromosome:
  - `mf_params_cls=None` (default): unchanged rule-only behaviour, exactly
    as before this parameter existed.
  - `mf_params_cls=<class>`: the chromosome becomes
    [rule block][MF block] (see chromosome.py's build_combined_chromosome/
    split_combined_chromosome for the exact layout), with MF-block bounds
    derived by default from mf_chromosome_bounds(mf_params_cls, schema) --
    i.e. every MF gene's search range is exactly its owning variable's
    declared domain, with no hardcoding.

There is deliberately no separate `optimize_mf_params: bool` flag --
`mf_params_cls` being set *is* the switch, so there's no way for a flag and
a class to disagree with each other.

`mf_bounds_fn` overrides where the MF block's bounds (and hence its LENGTH)
come from. The default, mf_chromosome_bounds, emits exactly 4 bounds per
dataclass field, so mf_len is always n_fields * 4 -- one gene per trapezoid
breakpoint, the raw-breakpoint parameterisation. A codec that optimises
something other than raw breakpoints (e.g. a constrained per-term
shift/scale plus a per-variable FOU-width multiplier) has a chromosome that
is not n_fields * 4 long at all, and cannot be expressed through the default.
Passing mf_bounds_fn(mf_params_cls, schema) -> (lower, upper) frees mf_len
from that shape entirely: nothing else in this module assumes it, since
sampling, mutation, splitting and artefact-saving all take mf_len as a plain
int. The only contract is that mf_params_cls.from_vector accepts a vector of
whatever length the bounds imply.

`mf_seed_fn`, if given, is a callable() -> an instance of mf_params_cls
(e.g. an expert IT2MFParams built via make_it2_from_t1), used to seed the
MF block of individual 0 via its to_vector(). Meaningless without
mf_params_cls also set; passing it alone raises rather than being silently
ignored.

GA design
---------
Backend: pymoo (CPU).
Representation: float-coded with bounds from the chromosome codec (rule
  block) and mf_chromosome_bounds (MF block, when present); rounding inside
  codec.decode converts the rule block to valid integer genes, while the MF
  block stays float (trapezoid breakpoints) all the way through.
Initialisation: first individual seeded from rules_fn / mf_seed_fn (if
  given) via codec.expert_chromosome() / mf_params_cls_instance.to_vector();
  remainder drawn uniformly at random within each block's own bounds.
Crossover: single-point crossover.
Mutation: polynomial mutation, applied to every individual, at a per-gene rate
  of 1/block_len calibrated separately for each block -- 1/rule_len over the
  rule block and 1/mf_len over the MF block, so a longer chromosome does not
  dilute rule-gene mutation pressure. See _make_mutation.
Selection: tournament selection (pymoo GA default).
"""

